//写一个宏，可以将一个整数的二进制位的奇数位和偶数位交换。


#define SwapIntBit(n) (((n) & 0x55555555) << 1 | ((n) & 0xaaaaaaaa) >> 1)
//00000000000000000000000000101010
//01010101010101010101010101010101  拿奇数位
//10101010101010101010101010101010  拿偶数位
//想拿出哪一位，就把那一位的数按位与1
#include<stdio.h>
int main()
{
	int n = 0;
	scanf("%d", &n);
	SwapIntBit(n);
	return 0;
}